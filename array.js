
// Array Manipulation Method
// pop - It removes an element from end of an array

let cars =["Mercedes", "BMW", "Audi"]
let poppedcar = cars.pop()
console.log(poppedcar)


console.log(cars)

// push - It adds an element at the end of an array
cars.push('Alto')
console.log(cars)


// Shift - It adds an element at the start of an array
let Shiftedcar = cars.shift()
console.log(Shiftedcar)

// unshift - It removes an element at the start of an array

cars.unshift('Alto')
console.log(cars    )


